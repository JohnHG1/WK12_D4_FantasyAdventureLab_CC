public abstract class Characters {

    protected String name;
    protected int power;

    public Characters(String name) {
        this.name = name;
        this.power = 150;
    }

    public String getName(){
        return this.name;
    }

    public int getPower(){
        return this.getPower();
    }

    public void setPower(int power){
        this.power = power;
    }



}
